/*5. Составить программу: определения наименьшего из двух чисел а и b.*/

public class Task05 {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 5;
		
		if (a < b) {
			System.out.println("Наименьшее число a = " + a);
		} else {
			System.out.println("Наименьшее число b = " + b);
		}

	}

}

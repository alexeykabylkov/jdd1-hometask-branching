/*6. Составить программу: определения наибольшего из двух чисел а и b.*/

public class Task06 {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 5;
		
		if (a > b) {
			System.out.println("Наибольшее число a = " + a);
		} else {
			System.out.println("Наибольшее число b = " + b);
		}

	}

}

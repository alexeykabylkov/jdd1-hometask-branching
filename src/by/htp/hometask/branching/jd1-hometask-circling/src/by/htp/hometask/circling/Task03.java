package by.htp.hometask.circling;

/*3. Необходимо вывести на экран таблицу умножения на 3: */

public class Task03 {

	public static void main(String[] args) {
		
		int i = 1;
		int k;
		
		while (i <= 10) {
			k = i * 3;
			System.out.println(i + " * 3 = " + k);
			i++;
		}

	}

}
